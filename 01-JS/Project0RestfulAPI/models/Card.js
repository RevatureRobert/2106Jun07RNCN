export class tradingCard{
    constructor(name, cost, color, attribute, job, type, subtype, attack, defense, description){
        this.name = name;
        this.cost = cost;
        this.color = color;
        this.attribute = attribute;
        this.job = job;
        this.type = type;
        this.subtype = subtype;
        this.attack = attack;
        this.defense = defense;
        this.description = description;
    }
}